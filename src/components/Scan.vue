<template>
  <v-container>
    <v-btn 
    color="success"
    @click="openScanner">
     scan
  </v-btn>
  <div id="barC"></div>
  </v-container>
</template>

<script>
import Quagga from 'quagga';
export default {
  data() {
    return {

    }
  },
  methods: {
    openScanner() {
      Quagga.init({
        inputStream : {
          name : "Live",
          type : "LiveStream",
          target: document.querySelector('#barC')    // Or '#yourElement' (optional)
        },
        decoder : {
          readers : ["code_128_reader"]
        }
      }, function(err) {
          if (err) {
              console.log(err);
              return
          }
          console.log("Initialization finished. Ready to start");
          Quagga.start();
      });
    }
  }
}
</script>

